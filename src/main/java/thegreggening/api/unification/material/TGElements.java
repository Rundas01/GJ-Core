    private static Element registerIsotope(Element baseIsotope, long nucleons) {
        long protons = baseIsotope.getProtons();
        return add(protons, nucleons - protons, -1, null, baseIsotope.getName() + "-" + nucleons,
                baseIsotope.getSymbol() + "-" + nucleons, true);
    }
